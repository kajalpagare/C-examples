/* Calculation of simple interest */

#include<stdio.h>
#include<conio.h>

void main()
{
	int p,n;
	float r,si;

	p = 1000;
	n = 3;
	r = 8.5;

	/* formula for simple interest */
	si = p * n * r / 100;

	printf("%f\n", si);
	printf("%d\n%d\n%f\n%f\n",p,n,r,si);
	printf("Simple interest= Rs.%f\n",si);
	printf("Principle=%d\nRate=%f",p,r);

	getch();
	clrscr();
}